CanvasRenderingContext2D.prototype.getSupportedChars = function() {
    var supported = [];
    for(var prop in this.pixelChars) {
        if(this.pixelChars.hasOwnProperty(prop)) {
            supported.push(prop);
        }
    }

    return supported;
};

CanvasRenderingContext2D.prototype.fillNumber = function(num, x, y) {
    var str = "" + Math.round(num);
    this.fillPixelText(str, x, y);
};

CanvasRenderingContext2D.prototype.pixelSize = 2;

CanvasRenderingContext2D.prototype.fillPixelText = function(str, x, y) {
    var baseX = x;
    for(var i = 0; i < str.length; i++) {
        var c = str.charAt(i);
        if(c == '\n') {
            baseX = x;
            y += this.pixelSize * 9;
        }

        var mat = ctx.pixelChars[c];
        if(typeof mat === 'undefined') {
            console.log("Couldn't find " + str.charAt(i) + ", continuing");
            continue;
        }

        for(var j = 0, y_ = y - this.pixelSize * 5; j < mat.length; j++, y_ += this.pixelSize) {
            var arr = mat[j];
            for(var k = 0, x_ = baseX; k < arr.length; k++, x_ += this.pixelSize) {
                if(arr[k]) {
                    ctx.fillRect(x_, y_, this.pixelSize, this.pixelSize);
                }
            }
        }

        baseX += this.pixelSize * (mat[0].length + 1);
    }
}